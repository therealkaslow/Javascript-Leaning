//abaixo temos uma estrutura de controle que é o "switch" que é uma tomada de decisão que pela lógica funciona como casos diferentes.
 
let numero = 5;

while (numero < 50) {
    console.log(numero);
    //ou seja, a incrementacao acrescenta mais um valor a ela mesma ou pode subtrair...
    numero += 10;
}
// um outro exemplo de incrementacao também é usar no laco de repeticão "for".
for (let i = 0; i < 10; i++) {
    console.log(`O valor de i é ${i}`);
}