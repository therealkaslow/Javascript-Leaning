//Uma técnica muito utilizada na programação é o loop nos arrays
//como é uma lista com muitos valores, muitas vezes precisamos ver cada um deles
let nomes = ["lucas", "marcelo", "joao"]

for(let i = 0; i <= nomes.length; i++){
    console.log(nomes[i]); // esse valor de "i" é transformado em índice onde o loop consegue verificar cada índice do array   
}

while(nomes.length > 0){
    console.log(nomes[i]);
}