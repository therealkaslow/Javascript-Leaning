//destructuring com arrays
//podemos definir variáveis com propiedades dos arrays com uma notação
//diferente, chamada destructuring, que cria variáveis apartir do array

const nomes = ["lucas", "matheus", "joao"];

const [nomeA, nomeB, nomeC] = nomes;

console.log(nomeA);
console.log(nomeB);
console.log(nomeC);
//ou seja o destructuring é desestruturar todo o array em variável